package com.harman.batch4;

public class ParentCalculator {
    public int AddOperation(int x,int y){
        return x+y;
    }
    public int SubOperation(int x,int y) {
        return x - y;
    }
    public int MultiplyOperation(int x,int y){
        return x * y;
    }
    public int DivideOperation(int x,int y){
        return x/y;
    }


}
